#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{	
	int	i;

	i = 0;
	if (!lst)
		return (NULL);
	while (lst)
	{
		if (lst->next == NULL)
			return (lst);
		lst = lst->next;
	}
	return (lst);
}
