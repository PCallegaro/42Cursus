#include "libft.h"

int	ft_atoi(const char *str)
{
	int	res;
	int	sign;

	res = 0;
	sign = 1;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-')
		sign *= -1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		if (res > 2147483647)
			return (-1);
		if (res < -2147483647)
			return (0);
		res = res * 10 + *str - '0';
		str++;
	}
	return (res * sign);
}
